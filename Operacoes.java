public class Operacoes {
    public void sum(int x, int y){
        System.out.println(x + y);
    }
    static void sub(int x, int y){
        System.out.println(x - y);
    }
    static void mult(int x, int y){
        System.out.println(x * y);
    }
    static void divi(int x, int y){
        System.out.println(x / y);
    }
}
